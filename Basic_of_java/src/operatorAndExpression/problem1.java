package operatorAndExpression;

// create programs to use all the Arthimetic operations

public class problem1 {
  public static void main(String[] args) {
	
	  int a = 10;
	  int b = 2;
	  
	  // 1 Additive operators
	   int sum = a+b;
	   System.out.println("the sum of a and b - " + sum);
	   
	  // 2 subtraction
	   int sub = a-b;
	   System.out.println("the subtraction of a and b - " + sub);
	   
	  // 3 Multiplication
	   int mul = a*b;
	   System.out.println("The multiplication of a and b - " + mul);
	   
	  // 4 division operator
	   int div  = a/b;
	   System.out.println("The division of a and b  - " + div);
	   
	  // 5 remainder operator
	   int rem  = a%b;
	   System.out.println("The remainder of a and b - " + rem);
	   
	  
}	

}
